#!/usr/bin/env node
import fs from 'node:fs/promises';
import path from 'node:path';

const PLAN_DIR = path.resolve('plan');
const STATUS_RX = /^\*\*Status\*\*:\s*(Shipped|In Progress|Planning|Superseded)/m;
const SUPERSEDED_RX = /^\*\*Superseded by\*\*:\s*(plan\/\d+\.md)/m;
const TITLE_RX = /^#\s+(?:Plan:\s*)?(.+?)(?:\s*—\s*`plan\/\d+\.md`)?$/m;

async function readPlan(file) {
  const text = await fs.readFile(path.join(PLAN_DIR, file), 'utf8');
  const status = text.match(STATUS_RX)?.[1] ?? 'Planning';
  const title = text.match(TITLE_RX)?.[1] ?? file.replace('.md', '');
  const supersededBy = text.match(SUPERSEDED_RX)?.[1] ?? null;
  return { file, status, title, supersededBy };
}

function render(plans) {
  const buckets = { Shipped: [], 'In Progress': [], Planning: [], Superseded: [] };
  for (const p of plans.sort((a, b) =>
    a.file.localeCompare(b.file, undefined, { numeric: true }),
  )) {
    buckets[p.status].push(p);
  }
  const section = (label, list) =>
    list.length === 0
      ? ''
      : `## ${label}\n\n${list
          .map((p) => {
            const link = `[${p.file}](./${p.file})`;
            const extra = p.supersededBy
              ? ` _(superseded by [${p.supersededBy}](./${p.supersededBy}))_`
              : '';
            return `- ${link} — ${p.title}${extra}`;
          })
          .join('\n')}\n`;
  return [
    '# Plan Index',
    '',
    '_Generated by `npm run plan:index`. Do not hand-edit._',
    '',
    section('Shipped', buckets.Shipped),
    section('In Progress', buckets['In Progress']),
    section('Planning', buckets.Planning),
    section('Superseded', buckets.Superseded),
  ]
    .join('\n')
    .replace(/\n{3,}/g, '\n\n');
}

const files = (await fs.readdir(PLAN_DIR)).filter((f) => /^\d+\.md$/.test(f));
const plans = await Promise.all(files.map(readPlan));
const out = render(plans);

const check = process.argv.includes('--check');
const target = path.join(PLAN_DIR, 'README.md');
if (check) {
  const current = await fs.readFile(target, 'utf8').catch(() => '');
  if (current !== out) {
    process.stderr.write('plan/README.md is stale. Run `npm run plan:index`.\n');
    process.exit(1);
  }
} else {
  await fs.writeFile(target, out);
}
